# Logical Tests in R:
# x < y  -->  less than
# x > y  -->  greater than
# x == y -->  equal to
# x >= y --> greater than or equal to  
# x <= y --> less than or equal to
# x != y --> not equal to
# x %in% y --> group membership
# is.na(x) -->  is NA
# !is.na(x) --> is not NA
# a & b --> a and b
# a | b --> a or b
# x or (a,b) -->  exactly or
# !a --> not a
